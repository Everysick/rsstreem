#[derive(Debug, PartialEq)]
pub enum Biop {
    Plus,
    Minus,
    Mul,
    Div,
    And,
    OR,
    Eq,
    Neq,
    Greater,
    Less,
    Over,
    Under,
}

#[derive(Debug, PartialEq)]
pub enum Ast {
    Int(i64),
    Float,
    Time,
    Str,
    Nil,
    Bool,
    Args,
    Pair,
    Cfunc,
    Lambda,
    Plambda,
    Parray,
    Pstruct,
    Psplat,
    Splat,
    Ident,
    Let,
    If,
    Emit,
    Skip,
    Return,
    Var,
    Const,
    Op {
        op: Biop,
        lhs: Box<Ast>,
        rhs: Box<Ast>,
    },
    Call,
    Fcall,
    Genfunc,
    Array,
    Nodes,
    Ns,
    Imort,
}
